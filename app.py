import streamlit as st

def es_primo(n):
    """
    Funci√≥n para verificar si un n√∫mero es primo.
    Un n√∫mero primo es un n√∫mero natural mayor que 1 que no tiene divisores positivos
    m√°s que 1 y √©l mismo.
    """
    # Los n√∫meros primos deben ser mayores que 1.
    if n <= 1:
        return False
    # El 2 es el √∫nico n√∫mero primo par.
    if n == 2:
        return True
    # Si es divisible por 2 (y no es 2), no es primo.
    if n % 2 == 0:
        return False
    # Verificamos divisores impares desde 3 hasta la ra√≠z cuadrada del n√∫mero.
    # Solo necesitamos comprobar hasta la ra√≠z cuadrada porque si n tiene un divisor
    # mayor que su ra√≠z cuadrada, entonces necesariamente tiene un divisor
    # m√°s peque√±o que su ra√≠z cuadrada.
    i = 3
    while i * i <= n:
        if n % i == 0:
            return False
        i += 2
    return True

# --- Interfaz de Usuario de Streamlit ---

# T√≠tulo de la aplicaci√≥n que se mostrar√° en la web.
st.title('üî¢ Verificador de N√∫meros Primos')

# Descripci√≥n o instrucciones para el usuario.
st.write("Introduce un n√∫mero entero para saber si es primo o no. Los n√∫meros primos son aquellos que solo son divisibles por 1 y por s√≠ mismos.")

# Campo de entrada num√©rico para que el usuario ingrese el n√∫mero.
# Usamos st.number_input para asegurarnos de que solo se introduzcan n√∫meros.
# 'min_value=0' para no permitir negativos. 'step=1' para que sean enteros.
numero_usuario = st.number_input('Ingresa un n√∫mero:', min_value=0, step=1, value=0)

# Bot√≥n para iniciar la verificaci√≥n.
if st.button('Verificar si es primo'):
    # Cuando el usuario haga clic en el bot√≥n, se ejecutar√° este bloque.
    if es_primo(numero_usuario):
        # Si la funci√≥n devuelve True, mostramos un mensaje de √©xito.
        st.success(f"¬°El n√∫mero {numero_usuario} es un n√∫mero primo! üéâ")
    else:
        # Si la funci√≥n devuelve False, mostramos un mensaje de advertencia/error.
        st.warning(f"El n√∫mero {numero_usuario} no es un n√∫mero primo. üòï")

